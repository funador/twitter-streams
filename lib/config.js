///////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Topics to track and domains to ignore
///////////////////////////////////////////////////////////////////////////////////////////////////////////////

exports.topics = ['nfl', 'nba', 'mlb', 'nhl', 'js', 'javascript']
exports.ignoredDomains = ['twitter.com', 'itunes', 'apple', 'vine.co', 'youtube', 'instagram']
exports.ingornedLanguages = ['es', 'fr', 'ru', 'fi', 'tr']

///////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Twitter Config
///////////////////////////////////////////////////////////////////////////////////////////////////////////////

const Twit = require('twit') 

const T = new Twit({

  consumer_key: process.env.CONSUMER_KEY,
  consumer_secret: process.env.CONSUMER_SECRET,
  access_token: process.env.ACCESS_TOKEN,
  access_token_secret: process.env.ACCESS_TOKEN_SECRET

})

exports.stream = T.stream('statuses/filter', { track: topics, language: 'en' })


///////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Firebase Config
///////////////////////////////////////////////////////////////////////////////////////////////////////////////

import * as firebaseConfig from "firebase"

const config = {
  apiKey: process.env.API_KEY,
  databaseURL: process.env.DATABASE_URL,
  storageBucket: process.env.STORAGE_BUCKET,
}

firebaseConfig.initializeApp(config)

exports.firebase = firebaseConfig.database()
